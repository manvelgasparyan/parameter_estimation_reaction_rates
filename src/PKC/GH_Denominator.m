function [g] = GH_Denominator (x, K)
   %g(i) is the rational term of the jth reaction rate
   %---------------------------------------------
   g(1) = 1 + x(1)/K(1) + x(2)/K(2);
   g(1) = 1/g(1);
   %---------------------------------------------
   g(2) = 1 + x(3)/K(3) + x(2)/K(4) + x(1)/K(5); 
   g(2) = 1/g(2);
   %---------------------------------------------
   g(3) = g(1);
   %---------------------------------------------
   g(4) = g(2);
   %---------------------------------------------
   g = g';
end
