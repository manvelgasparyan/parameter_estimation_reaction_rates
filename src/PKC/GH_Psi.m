function [output] = GH_Psi (x, v, K, S)
%-----------------------------------
   [s,r] = size(S);
%-----------------------------------   
   output = zeros(s,1);
%-----------------------------------   
   dx_dt = S*v;
%-----------------------------------   
   grad_g = GH_Nabla_g (x,K);
%-----------------------------------   
   for i = 1:r
       N_i = grad_g(i,:);
       output(i) = dot(N_i, dx_dt);
   end
%-----------------------------------   
end
