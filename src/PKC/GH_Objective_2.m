function [Error] = GH_Objective_2 (x_hat, k, K_hat, S, mu_1, T)
   %------------------------------------------------------------
   x_0 = x_hat(:,1);
   %------------------------------------------------------------
   t_span = 0:T/mu_1:T;
   %------------------------------------------------------------
   f = @(t,x)GH_Balance_Laws (t, x, k, K_hat, S);
   %------------------------------------------------------------
   [~,x] = ode23tb(f, t_span, x_0);
   %------------------------------------------------------------
   x = x';
   %------------------------------------------------------------
   Error = GH_Least_Squares_Error (x, x_hat);
end
